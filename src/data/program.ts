export function getAllWeeks(): Week[] {
  return months.flatMap((m) => m.weeks);
}

export function getWeekTitle(weekNumber: number): string {
  const week = getAllWeeks().find((w) => w.number === weekNumber);
  return week?.title ?? `Неделя ${weekNumber}`;
}

export function getCurrentWeek(startDate: string): number {
  const start = new Date(startDate);
  const now = new Date();
  const diffMs = now.getTime() - start.getTime();
  const diffWeeks = Math.ceil(diffMs / (7 * 24 * 60 * 60 * 1000));
  return Math.max(1, Math.min(12, diffWeeks));
}
